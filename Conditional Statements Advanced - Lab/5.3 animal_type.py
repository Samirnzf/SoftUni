# Read user input
animal = input()
user_output = 'unknown'

# Logic
if animal == 'dog':
    user_output = 'mammal'
elif animal == 'snake':
    user_output = 'reptile'
elif animal == 'crocodile':
    user_output = 'reptile'
elif animal == 'tortoise':
    user_output = 'reptile'

# Print output
print(user_output)